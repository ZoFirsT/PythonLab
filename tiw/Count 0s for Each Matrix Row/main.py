# รับค่าขนาดของเมทริกซ์
m = int(input())  # จำนวนแถว
n = int(input())  # จำนวนคอลัมน์

# สร้างเมทริกซ์ว่าง
matrix = []

# รับค่าสมาชิกของเมทริกซ์
for i in range(m):
    row = []
    for j in range(n):
        row.append(int(input()))  # รับค่าแต่ละสมาชิกในแถว
    matrix.append(row)  # เพิ่มแถวลงในเมทริกซ์

# แสดงเมทริกซ์ที่รับมา
print(matrix)

# สร้างลิสต์เก็บจำนวนเลข 0 ในแต่ละคอลัมน์
zero_counts = [0] * n

# นับจำนวนเลข 0 ในแต่ละคอลัมน์
for i in range(m):  # วนลูปผ่านทุกแถวของเมทริกซ์
    for j in range(n):  # วนลูปผ่านทุกคอลัมน์ในแต่ละแถว
        if matrix[i][j] == 0:  # ตรวจสอบว่าสมาชิกปัจจุบันเป็น 0 หรือไม่
            zero_counts[j] += 1  # ถ้าเป็น 0 ให้เพิ่มค่าตัวนับสำหรับคอลัมน์นั้นขึ้น 1

# อธิบายเพิ่มเติม:
# - เราใช้ลูปซ้อนเพื่อเข้าถึงทุกสมาชิกในเมทริกซ์
# - i คือดัชนีของแถว, j คือดัชนีของคอลัมน์
# - matrix[i][j] คือสมาชิกที่ตำแหน่ง (i, j) ในเมทริกซ์
# - zero_counts[j] เก็บจำนวนของ 0 ในคอลัมน์ที่ j
# - เมื่อพบ 0 ในคอลัมน์ใด ก็เพิ่มค่าตัวนับของคอลัมน์นั้น

# แสดงผลลัพธ์
print(*zero_counts)  # ใช้ * เพื่อแยกสมาชิกในลิสต์ออกมาพิมพ์
