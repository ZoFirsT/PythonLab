hours_worked = int(input())

if hours_worked <= 40:
    compensation = hours_worked * 50
else:
    extra_hours = hours_worked - 40
    compensation = (40 * 50) + (extra_hours * 75)

print(compensation)


# บรรทัดที่ 1: hours_worked = int(input())
# hours_worked: สร้างตัวแปรชื่อ hours_worked เพื่อเก็บจำนวนชั่วโมงที่ทำงาน
# int(input()):
# input() ทำหน้าที่รับค่าป้อนเข้าจากผู้ใช้ (ในที่นี้คือจำนวนชั่วโมงที่ทำงาน) ในรูปแบบของข้อความ (string)
# int() ทำการแปลงค่าที่ได้รับจาก input() ให้เป็นจำนวนเต็ม (integer) เพื่อนำไปใช้ในการคำนวณ
# บรรทัดที่ 2-6: if hours_worked <= 40:
# โครงสร้าง if-else: ใช้สำหรับตัดสินใจว่าจะดำเนินการส่วนไหนของโค้ด ขึ้นอยู่กับเงื่อนไขที่กำหนด
# hours_worked <= 40: เป็นเงื่อนไขที่ตรวจสอบว่าจำนวนชั่วโมงที่ทำงานน้อยกว่าหรือเท่ากับ 40 ชั่วโมงหรือไม่
# บรรทัดที่ 3: compensation = hours_worked * 50
# compensation: สร้างตัวแปรชื่อ compensation เพื่อเก็บค่าตอบแทน
# hours_worked * 50: คำนวณค่าตอบแทน โดยคูณจำนวนชั่วโมงที่ทำงานด้วยอัตราค่าจ้าง 50 บาทต่อชั่วโมง (สำหรับกรณีที่ทำงานไม่เกิน 40 ชั่วโมง)
# บรรทัดที่ 5: extra_hours = hours_worked - 40
# extra_hours: สร้างตัวแปรชื่อ extra_hours เพื่อเก็บจำนวนชั่วโมงที่ทำงานเกิน 40 ชั่วโมง
# hours_worked - 40: คำนวณหาจำนวนชั่วโมงที่ทำงานเกิน 40 ชั่วโมง
# บรรทัดที่ 6: compensation = (40 * 50) + (extra_hours * 75)
# compensation: คำนวณค่าตอบแทนใหม่ โดยแบ่งเป็นสองส่วน:
# (40 * 50): ค่าตอบแทนสำหรับ 40 ชั่วโมงแรก (อัตราชั่วโมงละ 50 บาท)
# (extra_hours * 75): ค่าตอบแทนสำหรับชั่วโมงที่ทำงานเกิน 40 ชั่วโมง (อัตราชั่วโมงละ 75 บาท)
# บรรทัดที่ 7: print(compensation)
# print(): แสดงผลค่าของตัวแปร compensation ซึ่งเป็นค่าตอบแทนทั้งหมดที่คำนวณได้
# สรุปการทำงานของโค้ด
# โค้ดนี้ทำหน้าที่คำนวณค่าตอบแทนจากจำนวนชั่วโมงที่ทำงาน โดยมีเงื่อนไขว่า หากทำงานไม่เกิน 40 ชั่วโมง จะใช้อัตราค่าจ้าง 50 บาทต่อชั่วโมง แต่ถ้าทำงานเกิน 40 ชั่วโมง ชั่วโมงที่เกินมาจะใช้อัตราค่าจ้าง 75 บาทต่อชั่วโมง

