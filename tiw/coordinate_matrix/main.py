# รับค่า input จากผู้ใช้
n = int(input("ป้อนขนาดของเมทริกซ์ (n x n): "))
x = int(input("ป้อนค่า x ของจุดที่ต้องการหยุด: "))
y = int(input("ป้อนค่า y ของจุดที่ต้องการหยุด: "))

# วนลูปสร้างเมทริกซ์
for i in range(n):
    for j in range(n):
        # พิมพ์พิกัดในรูปแบบ (i, j)
        print(f"({i}, {j})", end=" ")
        
        # ตรวจสอบว่าถึงจุดที่ต้องการหยุดหรือไม่
        if i == x and j == y:
            break  # หยุดการทำงานของลูปภายใน
    
    print()  # ขึ้นบรรทัดใหม่หลังจากพิมพ์แต่ละแถวเสร็จ
    
    # ตรวจสอบอีกครั้งเพื่อหยุดการทำงานของลูปภายนอก
    if i == x:
        break