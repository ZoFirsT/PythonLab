# บรรทัดที่ 4-9: ตรวจสอบ Quadrant
# โครงสร้าง if-elif-else: ใช้สำหรับตรวจสอบเงื่อนไขหลายๆ เงื่อนไข
# เงื่อนไขแต่ละข้อ:
# if x > 0 and y > 0: : ถ้า x มากกว่า 0 และ y มากกว่า 0 แสดงว่าจุดนั้นอยู่ใน Quadrant ที่ 1
# elif x < 0 and y > 0: : ถ้า x น้อยกว่า 0 และ y มากกว่า 0 แสดงว่าจุดนั้นอยู่ใน Quadrant ที่ 2
# elif x < 0 and y < 0: : ถ้า x น้อยกว่า 0 และ y น้อยกว่า 0 แสดงว่าจุดนั้นอยู่ใน Quadrant ที่ 3
# else: : ถ้าไม่ตรงกับเงื่อนไขใดๆ ข้างต้น แสดงว่าจุดนั้นอยู่ใน Quadrant ที่ 4
# บรรทัดที่ 10: แสดงผล Quadrant
# print(quadrant) : แสดงผล Quadrant ที่ได้จากการตรวจสอบ
# บรรทัดที่ 12: กรณีที่ x หรือ y เป็น 0
# else: : ถ้าค่า x หรือ y ใดๆ เป็น 0 จะเข้ามาในส่วนนี้
