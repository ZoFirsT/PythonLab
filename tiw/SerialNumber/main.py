# รับค่า x และ y จากผู้ใช้
x = int(input())  # รับค่า x และแปลงเป็นจำนวนเต็ม
y = int(input())  # รับค่า y และแปลงเป็นจำนวนเต็ม

# สร้างลิสต์เปล่าเพื่อเก็บผลลัพธ์
result = []

# วนลูปตั้งแต่ 1 ถึง y
for i in range(1, y + 1):
    # สร้างเทอมโดยการทำซ้ำ x ตาม i แล้วแปลงเป็นจำนวนเต็ม
    term = int(str(x) * i)
    # เพิ่มเทอมลงในลิสต์ผลลัพธ์ (แปลงเป็นสตริง)
    result.append(str(term))

# พิมพ์ผลลัพธ์โดยใช้เครื่องหมายคอมม่าคั่นระหว่างแต่ละเทอม
print(','.join(result))

