# รับค่าตัวเลขสองตัวจากผู้ใช้
a = int(input())  # รับค่าตัวเลขตัวแรกและแปลงเป็นจำนวนเต็ม
b = int(input())  # รับค่าตัวเลขตัวที่สองและแปลงเป็นจำนวนเต็ม

# ตรวจสอบว่า a มากกว่า b หรือไม่
if a > b:
    a, b = b, a  # สลับค่า a และ b ถ้า a มากกว่า b เพื่อให้ a เป็นตัวเลขที่น้อยกว่าเสมอ

# ใช้อัลกอริทึมยูคลิดเพื่อหา GCD
while b != 0:  # ทำซ้ำจนกว่า b จะเป็น 0
    a, b = b, a % b  # กำหนดค่า a เป็น b และ b เป็นเศษที่ได้จากการหาร a ด้วย b

# พิมพ์ค่า GCD ที่ได้
print(a)  # a คือค่า GCD ที่ต้องการ
