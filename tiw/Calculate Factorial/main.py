# ฟังก์ชัน factorial คำนวณค่าแฟกทอเรียลและสร้างนิพจน์แสดงการคำนวณ
def factorial(x):
    # กรณีพิเศษสำหรับ 0! และ 1! ซึ่งมีค่าเท่ากับ 1
    if x == 0 or x == 1:
        return "1"
    
    result = 1  # ตัวแปรเก็บผลลัพธ์การคูณ
    expression = ""  # สตริงเก็บนิพจน์แสดงการคำนวณ
    
    # วนลูปจาก x ถึง 1 (นับถอยหลัง)
    for i in range(x, 0, -1):
        result *= i  # คูณค่าปัจจุบันเข้ากับผลลัพธ์
        expression += str(i)  # เพิ่มตัวเลขปัจจุบันเข้าไปในนิพจน์
        if i > 1:
            expression += "*"  # เพิ่มเครื่องหมายคูณถ้ายังไม่ใช่ตัวสุดท้าย
    
    # เพิ่มเครื่องหมายเท่ากับและผลลัพธ์ท้ายนิพจน์
    expression += "=" + str(result)
    
    return expression  # ส่งคืนนิพจน์ที่สมบูรณ์

# รับค่า x จากผู้ใช้และแปลงเป็นจำนวนเต็ม
x = int(input())

# เรียกใช้ฟังก์ชัน factorial และพิมพ์ผลลัพธ์
print(factorial(x))